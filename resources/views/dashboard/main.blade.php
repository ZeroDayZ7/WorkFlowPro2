<h1>Witaj w panelu zarządzania!</h1>
<p>
    Ten dashboard jest centralnym punktem zarządzania Twoimi danymi i operacjami. Znajdziesz tutaj wszystkie narzędzia potrzebne do efektywnego zarządzania pracownikami oraz organizowania dokumentów.
</p>

<h2>Co możesz zrobić?</h2>
<ul>
    <li>
        <strong>Dodaj Pracownika:</strong> Kliknij na opcję "Dodaj Pracownika", aby zarejestrować nowego członka zespołu. Uzupełnij formularz, aby wprowadzić niezbędne dane.
    </li>
    <li>
        <strong>Dokumenty:</strong> W sekcji "Dokumenty" możesz przeglądać, dodawać i zarządzać istotnymi plikami oraz dokumentami związanymi z pracownikami i projektami.
    </li>
    <li>
        <strong>Ustawienia:</strong> W sekcji "Ustawienia" możesz dostosować preferencje aplikacji, zarządzać kontami użytkowników i kontrolować dostęp do różnych funkcji.
    </li>
</ul>

<h2>Jak to działa?</h2>
<p>
    Wszystkie funkcje są łatwo dostępne poprzez menu po lewej stronie. Kliknij odpowiednią opcję, aby przejść do żądanej sekcji. Twoje wybory będą na bieżąco aktualizowane na tej stronie, dzięki czemu możesz efektywnie zarządzać swoimi zadaniami.
</p>

